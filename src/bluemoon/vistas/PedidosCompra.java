    public void CargarPedidos(){
        DefaultTableModel objTabla = new DefaultTableModel();
        
        tblPedidos.setModel(objTabla);
        objTabla.setRowCount(0);
        objTabla.addColumn("ID_Pedido");
        objTabla.addColumn("ID_Proveedor");
        objTabla.addColumn("ID_Producto");
        objTabla.addColumn("Precio");
        objTabla.addColumn("Cantidad");
        
        List<Vector> listaPedidos = PedidoCompraDAO.findAll();
        
        for(Vector v : listaPedidos)
        {
            objTabla.addRow(v);
        }
    }
    
    public void CargarProductos(){
        DefaultComboBoxModel objCbo = new DefaultComboBoxModel();
        
        cbxProducto.setModel(objCbo);
        
        ResultSet resProd = Conexion.Consulta("select * from producto");
        
        try {
            while(resProd.next()){
                
                objCbo.addElement(resProd.getString(2));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la consulta de la tablas.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    

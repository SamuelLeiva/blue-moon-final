    public String BuscarIdProv(String nombre) throws SQLException{
        PreparedStatement pstProv = Conexion.getConexion().prepareStatement("SELECT * FROM PROVEEDOR WHERE NOMBRE_PROVEEDOR = ?");
        pstProv.setString(1, nombre);
        ResultSet res = pstProv.executeQuery();
         
        String id = "";
        
        while(res.next()){        
            id = res.getString(1);
        }
        return id;
    }
    
    public String BuscarIdProd(String nombre) throws SQLException{
        PreparedStatement pst = Conexion.getConexion().prepareStatement("SELECT * FROM PRODUCTO WHERE NOMBRE_PRODUCTO = ?");
        pst.setString(1, nombre);
        ResultSet res = pst.executeQuery();
         
        String id = "";
        
        while(res.next()){        
            id = res.getString(1);
        }
        return id;
    }
    
    public double BuscarPrecio(String id) throws SQLException{
        PreparedStatement pst = Conexion.getConexion().prepareStatement("SELECT * FROM PRODUCTO WHERE ID_PRODUCTO = ?");
        pst.setString(1, id);
        ResultSet res = pst.executeQuery();
         
        double precio = 0.0;
        
        while(res.next()){
            precio = res.getDouble(5);
        }
        return precio;
    }

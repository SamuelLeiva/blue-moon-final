    public void CargarInventario(String sql){
        DefaultTableModel objTabla = new DefaultTableModel();
        
        tblInventario.setModel(objTabla);
        objTabla.setRowCount(0);
        objTabla.addColumn("Producto");
        objTabla.addColumn("Stock");
        
        ResultSet resInv = Conexion.Consulta(sql); //HACER SELECT MULTIPLE PARA QUE SE VEA EL NOMBRE DEL PRODUCTO
        try {
            while(resInv.next()){
                Vector v = new Vector();
                v.add(resInv.getString(1));
                v.add(resInv.getInt(2));
                
                objTabla.addRow(v);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en la consulta de la tablas.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void ActualizarStock(String nombre, int stock) throws SQLException{
        String idProd = "";
        //buscar id del producto
        String sql = "select * from producto where nombre_producto=?";

        PreparedStatement pst = Conexion.getConexion().prepareStatement(sql);

        pst.setString(1,nombre);
        ResultSet resProd = pst.executeQuery();
        
        while(resProd.next()){
            idProd = resProd.getString(1);
        }
        
        PreparedStatement stmActualizar;
        stmActualizar = Conexion.getConexion().prepareStatement("UPDATE DET_INVENTARIO SET CANTIDAD=?"
                + "  WHERE ID_PRODUCTO=?");
        
        stmActualizar.setInt(1,stock);
        stmActualizar.setString(2,idProd);//id del producto
        stmActualizar.executeUpdate();
    }


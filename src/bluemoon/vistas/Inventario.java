    private void tblInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInventarioMouseClicked
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint( evt.getPoint() );
        String nombreProducto=source.getModel().getValueAt(row, 0)+""; //extrayendo nombre del producto
        String cantidadInv = source.getModel().getValueAt(row, 1)+""; //cantidad en stock
        
        txtNombre.setText(nombreProducto);
        txtCantidad.setText(cantidadInv);

        
    }//GEN-LAST:event_tblInventarioMouseClicked


